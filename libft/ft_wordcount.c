#include "libft.h"

int		ft_wordcount(char *str)
{
	int i;
	int ctr;

	ctr = 0;
	i = 0;
	while (str[i])
	{
		if ((str[i] != ' ' && str[i + 1] == ' ')
				|| (str[i] != ' ' && str[i + 1] == 0))
			ctr++;
		i++;
	}
	return (ctr);
}
