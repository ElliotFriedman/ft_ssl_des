#include "libft.h"

char	*ft_strcat(char *s1, const char *src)
{
	int		i;
	int		x;

	i = -1;
	x = -1;
	while (s1[++i])
		;
	while (src[++x])
		s1[i++] = src[x];
	s1[i] = '\0';
	return (s1);
}
