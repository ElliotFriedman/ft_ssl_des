#include "libft.h"

void	ft_putnbr(int n)
{
	char	a;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	if (n > -1 && n < 10 && n != -2147483648)
	{
		a = n + '0';
		write(1, &a, 1);
	}
	else if (n != -2147483648 && n > -1)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n < 0 && n != -2147483648)
	{
		write(1, "-", 1);
		ft_putnbr(n * -1);
	}
}
